// // basic for loop
for(let index = 1; index < 10; index++)
{
    console.log(index*index);
}



// // while loop

let index = 1;
while(index < 10)
{
    console.log(index * index);
    index++
}




// do-while loop

index = 1
do{
    console.log(`it will always print`);
    console.log(index * index);
    index++
}while(index < 10);
